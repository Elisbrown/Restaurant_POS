export type Language = "en" | "fr"
export type TranslationKey = keyof typeof translations.en

export function getTranslation(key: TranslationKey, language: Language): string {
  return translations[language][key] || translations.en[key]
}

// Enhanced translation function with interpolation
export function t(key: TranslationKey, language: Language, params?: Record<string, string | number>): string {
  let translation = getTranslation(key, language)

  if (params) {
    Object.entries(params).forEach(([paramKey, value]) => {
      translation = translation.replace(`{{${paramKey}}}`, String(value))
    })
  }

  return translation
}

// Pluralization helper
export function pluralize(
  key: TranslationKey,
  count: number,
  language: Language,
  params?: Record<string, string | number>,
): string {
  const pluralKey = count === 1 ? key : (`${key}Plural` as TranslationKey)
  return t(pluralKey, language, { ...params, count })
}
